const crearCartaInicial = (idFoto: number, imagen: string): Carta => ({
  idFoto,
  imagen,
  estaVuelta: false,
  encontrada: false,
});

export const crearColeccionDeCartasInicial = (
  infoCartas: InfoCarta[]
): Carta[] => {
  const coleccionDuplicada: InfoCarta[] = [...infoCartas, ...infoCartas];
  const coleccionFinal: Carta[] = coleccionDuplicada.map((infoCarta) => {
    return crearCartaInicial(infoCarta.idFoto, infoCarta.imagen);
  });
  return coleccionFinal;
};

